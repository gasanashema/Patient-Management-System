    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        // TODO add your handling code here:
    String patientID = patientIdtxt.getText().trim(); 
    String fullName = fullNameTxt.getText().trim();
    String gender = genderTxt.getText().trim();
    String diagnosis = diagnosisTxt.getText().trim();
    String ageText = ageTxt.getText().trim();
    String consultationText = consultationTxt.getText().trim();
    
    if(patientID.isEmpty()){
        JOptionPane.showMessageDialog(this,"Patient ID must not be empty","VALIDATION MESSAGE",JOptionPane.ERROR_MESSAGE);
    }
    else if(fullName.isEmpty()){
        JOptionPane.showMessageDialog(this,"Full name must not be empty","VALIDATION MESSAGE",JOptionPane.ERROR_MESSAGE);
    }
    else if(gender.isEmpty()){
        JOptionPane.showMessageDialog(this,"Gender must not be empty","VALIDATION MESSAGE",JOptionPane.ERROR_MESSAGE);
    }
   else if(diagnosis.isEmpty()){
        JOptionPane.showMessageDialog(this,"Diagnosis must not be empty","VALIDATION MESSAGE",JOptionPane.ERROR_MESSAGE);
    }
   else if(consultationText.isEmpty()){
        JOptionPane.showMessageDialog(this,"age must not be empty","VALIDATION MESSAGE",JOptionPane.ERROR_MESSAGE);
    }
    else if(consultationText.isEmpty()){
        JOptionPane.showMessageDialog(this,"consltation must not be empty","VALIDATION MESSAGE",JOptionPane.ERROR_MESSAGE);
     }
    
    else{
        int age = 0;
        double consultationFee = 0;
        
        try{
             age = Integer.parseInt(ageText);
        }catch(Exception e ){
            JOptionPane.showMessageDialog(this,"Age must be numeric");
            return;
        }
        try{
              consultationFee = Double.parseDouble(consultationText);
        }catch(Exception e ){
            JOptionPane.showMessageDialog(this,"Consultation Fee must be numeric");
            return;
        }
        
        if(!patientID.matches("^PAT-[0-9]{4}$")){
        JOptionPane.showMessageDialog(this,"Format must be PAT-####","VALIDATION MESSAGE",JOptionPane.ERROR_MESSAGE);
    }
        else if(!fullName.matches("^[a-zA-Z ]+$") || fullName.length()<5){
                 JOptionPane.showMessageDialog(this,"full name must have least 5 characters ","VALIDATION MESSAGE",JOptionPane.ERROR_MESSAGE);
        }
        else if(age<0 || age>120){
                 JOptionPane.showMessageDialog(this,"Age should be a number between 0 and 120","VALIDATION MESSAGE",JOptionPane.ERROR_MESSAGE);
        }
        else if(!diagnosis.matches("^[a-zA-Z]+$") ||  diagnosis.length()<5 ){
                 JOptionPane.showMessageDialog(this,"diagnosis must have least 5 characters","VALIDATION MESSAGE",JOptionPane.ERROR_MESSAGE);
        }
        else if (consultationFee<5000 || consultationFee>50000){
                JOptionPane.showMessageDialog(this,"consulstation fee should be a number between 5000 and 50000 ","VALIDATION MESSAGE",JOptionPane.ERROR_MESSAGE);
        }
        else if(!gender.equalsIgnoreCase("male") && !gender.equalsIgnoreCase("female")){
                JOptionPane.showMessageDialog(this," gender should be male or female"," VALIDATION MESSAGE",JOptionPane.ERROR_MESSAGE);
        }
        else{
            if(age<12){
                    double discount = consultationFee * 0.5;
                    consultationFee = consultationFee - discount;
                    JOptionPane.showMessageDialog(this,"discount is "+discount);
                    JOptionPane.showMessageDialog(this," new consultation fee is : "+consultationFee);
                }
                else if(age>60){
                    double discount = consultationFee * 0.3;
                    consultationFee = consultationFee - discount;
                    JOptionPane.showMessageDialog(this,"discount is " +discount);
                    JOptionPane.showMessageDialog(this," new consultation fee is : " +consultationFee);
                }
                else{
                    JOptionPane.showMessageDialog(this," no discount");
                }
            Patient patientObj = new Patient();
            patientObj.setPatientId(patientID);
            patientObj.setFullName(fullName);
            patientObj.setAge(age);
            patientObj.setGender(gender);
            patientObj.setDiagnosis(diagnosis);
            patientObj.setConsultationFee(consultationFee);
            patientObj.setRegistrationDate(LocalDate.now());
            
            // send to the dao 
            PatientDao dao = new PatientDao();
            int rowsAffected = dao.addPatient(patientObj);
            if(rowsAffected > 0){
                JOptionPane.showMessageDialog(this,"REGISTRATION OF PATIENT IS A SUCCESS");
            }
            else{
                    JOptionPane.showMessageDialog(this,"Registration of a Patient Failed");
            }
        }
    }
    
    }//GEN-LAST:event_registerBtnActionPerformed

