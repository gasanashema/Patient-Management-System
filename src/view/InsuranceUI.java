  // ================= TABLE REFRESH =================
    private void refreshTable() {
        List<InsuranceCompany> list = dao.findAllCompanies();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for (InsuranceCompany ic : list) {
            model.addRow(new Object[]{
                ic.getCompanyID(),
                ic.getCompanyName(),
                ic.getCoveragePercentage(),
                ic.getContactPhone(),
                ic.getContactEmail()
            });
        }
    }
    
     // ================= VALIDATION =================
    private boolean validateInputs() {

        if (txtCompanyName.getText().trim().isEmpty() ||
            txtCoverage.getText().trim().isEmpty() ||
            txtPhone.getText().trim().isEmpty() ||
            txtEmail.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(this, "All fields are required!");
            return false;
        }

        // Phone must be 10 digits
        if (!txtPhone.getText().matches("\\d{10}")) {
            JOptionPane.showMessageDialog(this, "Phone must be exactly 10 digits!");
            return false;
        }

        // Simple email validation
        if (!txtEmail.getText().contains("@gmail.com")) {
            JOptionPane.showMessageDialog(this, "Email must contain @gmail.com");
            return false;
        }

        // Coverage must be a number
        try {
            Double.parseDouble(txtCoverage.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Coverage must be a number!");
            return false;
        }

        return true;
    }

