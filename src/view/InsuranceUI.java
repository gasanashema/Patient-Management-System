// ================= ADD =================
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
  
        if (!validateInputs()) return;

        InsuranceCompany ic = new InsuranceCompany();
        ic.setCompanyName(txtCompanyName.getText());
        ic.setCoveragePercentage(Double.parseDouble(txtCoverage.getText()));
        ic.setContactPhone(txtPhone.getText());
        ic.setContactEmail(txtEmail.getText());

        int result = dao.addCompany(ic);

        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Added Successfully!");
            refreshTable();
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to Add");
        }
   


    }//GEN-LAST:event_btnAddActionPerformed
// ================= UPDATE =================
    private void UpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBtnActionPerformed

        int row = jTable1.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a row first!");
            return;
        }

        if (!validateInputs()) return;

        int id = (int) jTable1.getValueAt(row, 0);

        InsuranceCompany ic = new InsuranceCompany(
                id,
                txtCompanyName.getText(),
                Double.parseDouble(txtCoverage.getText()),
                txtPhone.getText(),
                txtEmail.getText()
        );

        int result = dao.updateCompany(ic);

        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Updated Successfully!");
            refreshTable();
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Update Failed");
        }

    }//GEN-LAST:event_UpdateBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
         int row = jTable1.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Select a row first!");
        return;
    }

    int id = (int) jTable1.getValueAt(row, 0);

    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete?");

    if (confirm == JOptionPane.YES_OPTION) {
        int result = dao.deleteCompany(id);

        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Deleted Successfully!");
            refreshTable();
        } else {
            JOptionPane.showMessageDialog(this, "Delete Failed");
        }
    }

        
    }//GEN-LAST:event_deleteBtnActionPerformed

